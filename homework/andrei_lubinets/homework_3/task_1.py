# Даны 2 числа a и b. Получить их сумму, разность и произведение

first_number = 15
second_number = 8

sum_of_numbers = first_number + second_number
subtraction_of_numbers = first_number - second_number
multiply_of_numbers = second_number * first_number


print(f"Результат сложения двух чисел: {sum_of_numbers}")
print(f"Результат разности двух чисел: {subtraction_of_numbers}")
print(f"Результат произведения двух чисел: {multiply_of_numbers}")
